// Build-time generator: snapshots the slugRedirect table into a static TS map
// that the Edge middleware (src/proxy.ts) can import. The Edge runtime can't talk
// to Postgres/Redis, so the redirect lookup has to be a plain in-bundle object.
// Run before `next build` (wired into deploy.sh).
require('dotenv').config({ quiet: true });
const fs = require('fs');
const path = require('path');

async function main() {
  const out = path.join(__dirname, '..', 'src', 'generated', 'slug-redirects.ts');
  fs.mkdirSync(path.dirname(out), { recursive: true });

  let product = {};
  let category = {};
  try {
    const { PrismaClient } = require('../generated/prisma');
    const { Pool } = require('pg');
    const { PrismaPg } = require('@prisma/adapter-pg');
    const pool = new Pool({ connectionString: process.env.DATABASE_URL });
    const prisma = new PrismaClient({ adapter: new PrismaPg(pool) });
    const rows = await prisma.slugRedirect.findMany({
      select: { oldSlug: true, newSlug: true, type: true },
    });
    for (const r of rows) {
      if (r.type === 'category') category[r.oldSlug] = r.newSlug;
      else product[r.oldSlug] = r.newSlug;
    }
    await prisma.$disconnect();
    await pool.end();
  } catch (e) {
    // Never fail the build over this — fall back to whatever already exists, or
    // empty maps. (e.g. DB unreachable in a CI/preview build.)
    console.warn('[gen-slug-redirects] DB read failed, writing empty maps:', e.message);
    if (fs.existsSync(out)) {
      console.warn('[gen-slug-redirects] keeping existing file');
      return;
    }
  }

  const body =
    '// AUTO-GENERATED by scripts/gen-slug-redirects.cjs — do not edit by hand.\n' +
    '// Refreshed on every deploy (see deploy.sh).\n' +
    `export const PRODUCT_SLUG_REDIRECTS: Record<string, string> = ${JSON.stringify(product, null, 2)};\n\n` +
    `export const CATEGORY_SLUG_REDIRECTS: Record<string, string> = ${JSON.stringify(category, null, 2)};\n`;
  fs.writeFileSync(out, body);
  console.log(
    `[gen-slug-redirects] wrote ${Object.keys(product).length} product + ${Object.keys(category).length} category redirects -> ${out}`,
  );
}

main().catch((e) => {
  console.error('[gen-slug-redirects] fatal:', e.message);
  process.exit(1);
});
