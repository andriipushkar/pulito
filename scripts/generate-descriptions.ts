function parseProduct(name: string, category: string, price: number): ProductData {
  const volMatch = name.match(/(\d+[.,]?\d*)\s*(мл|л|ml)/i);
  const volume = volMatch ? `${volMatch[1].replace(',', '.')} ${volMatch[2].toLowerCase() === 'л' || volMatch[2].toLowerCase() === 'l' ? 'л' : 'мл'}` : '';
  
  const weightMatch = name.match(/(\d+[.,]?\d*)\s*(кг|гр|g|kg)/i);
  const weight = weightMatch ? `${weightMatch[1].replace(',', '.')} ${weightMatch[2].toLowerCase().replace('g', 'гр').replace('kg', 'кг')}` : '';

  const washMatch = name.match(/(\d+)\s*прань?/i);
  const washes = washMatch ? washMatch[1] : '';

  const countMatch = name.match(/(\d+)\s*(шт|таб|капсул|штук)/i);
  const count = countMatch ? `${countMatch[1]} ${countMatch[2]}` : '';

  const brand = name.replace(/^New!?\s*/i, '').split(/\s+/)[0].replace(/[^A-Za-zА-Яа-яЄєІіЇїҐґ'.-]/g, '');

  let type = 'універсальний';
  if (/колор|color|colorat/i.test(name)) type = 'для кольорових тканин';
  else if (/біл|bianc|white|bianco/i.test(name)) type = 'для білих тканин';
  else if (/чорн|темн|dark|nero|scur|neri/i.test(name)) type = 'для темних та чорних тканин';
  else if (/делікат|lana|seta|capi delicati|шерст|wool|cachmere/i.test(name)) type = 'для делікатних тканин та шерсті';
  else if (/спорт|sport/i.test(name)) type = 'для спортивного одягу';
  else if (/дитяч|bebe|baby|chicco/i.test(name)) type = 'для дитячої білизни';
  else if (/гіпоалерг|non.?bio|sensitive/i.test(name)) type = 'гіпоалергенний';
  else if (/ply?amo|smacchi|stain/i.test(name)) type = 'з ефектом плямовивідника';

  let aroma = '';
  if (/лаванд|lavand/i.test(name)) aroma = 'лаванди';
  else if (/лимон|lemon|limone/i.test(name)) aroma = 'лимона';
  else if (/tropikal|tropical|тропік/i.test(name)) aroma = 'тропічних фруктів';
  else if (/floreale|flores|квіт|flower/i.test(name)) aroma = 'квітів';
  else if (/muschio|мускус|musk/i.test(name)) aroma = 'білого мускусу';
  else if (/orange|апельсин/i.test(name)) aroma = 'апельсинового цвіту';
  else if (/marsell?|марсел/i.test(name)) aroma = 'марсельського мила';
  else if (/talco|тальк/i.test(name)) aroma = 'тальку';
  else if (/argan/i.test(name)) aroma = 'арганової олії';
  else if (/aloe/i.test(name)) aroma = 'алое вера';
  else if (/ocean|океан/i.test(name)) aroma = 'океанської свіжості';
  else if (/spring|primaver|весн/i.test(name)) aroma = 'весняної свіжості';

  return { id: 0, name, category, price, volume: volume || weight || count, washes, brand, type, aroma, weight };
}

