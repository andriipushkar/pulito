async function main() {
  // Delete all existing content to regenerate
  const deleted = await prisma.productContent.deleteMany({});
  console.log(`Видалено старих описів: ${deleted.count}`);

  const products = await prisma.product.findMany({
    select: { id: true, name: true, priceRetail: true, category: { select: { name: true } } },
    orderBy: { id: 'asc' },
  });

  console.log(`Товарів для обробки: ${products.length}`);
  let created = 0, errors = 0;

  for (const product of products) {
    const catName = product.category?.name || 'Різне';
    const info = parseProduct(product.name, catName, Number(product.priceRetail));
    const generator = gen[catName] || gen['Різне'];
    const content = generator(info);

    try {
      await prisma.productContent.create({
        data: {
          productId: product.id,
          shortDescription: content.short.slice(0, 200),
          fullDescription: content.full,
          specifications: content.specs,
          usageInstructions: content.usage,
          isFilled: true,
        },
      });
      created++;
      if (created % 100 === 0) console.log(`  Створено: ${created}/${products.length}`);
    } catch (e: any) {
      errors++;
      if (errors <= 5) console.error(`  Помилка ID ${product.id}: ${e.message?.slice(0, 80)}`);
    }
  }

  console.log(`\nГотово! Створено: ${created} | Помилок: ${errors}`);
  await prisma.$disconnect();
}

main().catch(console.error);
